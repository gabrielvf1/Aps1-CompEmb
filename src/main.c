// Função de inic	ialização do uC
void init(void){
	 // Initialize the board clock
  sysclk_init();
  // Inicializa PIO do botao
  pmc_enable_periph_clk(BUT_ON_PAUSE_PIO_ID);
  pmc_enable_periph_clk(BUZ_PIO_ID);
  
  
  // Disativa WatchDog Timer
  WDT->WDT_MR = WDT_MR_WDDIS;

  // Ativa o PIO na qual o LED foi conectado
  // para que possamos controlar o LED.
  pmc_enable_periph_clk(LED_PIO_ID); 

  //Inicializa PC8 como saída
  pio_set_output(LED_PIO, LED_PIO_IDX_MASK, 0, 0, 0);
  pio_set_output(BUZ_PIO, BUZ_PIO_IDX_MASK, 0, 0, 0);
  // configura pino ligado ao botão como entrada com um pull-up.
  pio_set_input(BUT_ON_PAUSE_PIO,BUT_ON_PAUSE_PIO_IDX_MASK, 1);
 // pio_set_debounce_filter(BUT_ON_PAUSE_PIO,BUT_ON_PAUSE_PIO_IDX_MASK,10);
  
  _pio_pull_up(BUT_ON_PAUSE_PIO,BUT_ON_PAUSE_PIO_IDX_MASK,1);
}
void play_music(int duration[MAX_SIZE],int notes[MAX_SIZE],int size){
	for (int i=0;i<size;i++){              
		int wait = 1000*((double)duration[i] * songspeed);
		int noteduration = (double)duration[i]*notespeed;
		long delayValue = 1000000 / notes[i] / 2;

		if(!(pio_get(BUT_ON_PAUSE_PIO,PIO_INPUT,BUT_ON_PAUSE_PIO_IDX_MASK))){
			i=size;
			delay_ms(500);
			}else{
			_pio_set(LED_PIO,LED_PIO_IDX_MASK);
			for(int j=0;j<noteduration;j++){
				_pio_set(BUZ_PIO,BUZ_PIO_IDX_MASK);
				delay_us(delayValue);
				_pio_clear(BUZ_PIO,BUZ_PIO_IDX_MASK);
				delay_us(delayValue);
				
			}
			_pio_clear(LED_PIO,LED_PIO_IDX_MASK);
			delay_us(wait);
			
			
		}
		
		
		
	}
	
}
